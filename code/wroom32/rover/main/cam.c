void cam_write(cam_handle_t *cam_handle, uint8_t reg, uint8_t data){
    uint16_t reg16 = reg && 0x00FF;
    uint16_t data16 = data && 0x00FF;
    i2c_master_transmit(cam_handle->cam_i2c_handle, (void*) &reg16, sizeof(uint16_t), -1);
    i2c_master_transmit(cam_handle->cam_i2c_handle, (void*) &data16, sizeof(uint16_t), -1);
}

void cam_write_list(cam_handle_t *cam_handle, const sensor_data *data){
    uint16_t reg_addr = 0;
    uint16_t reg_val = 0;
    while ((reg_addr != 0xff) | (reg_val != 0xff))
    {
        reg_addr = data->reg;
        reg_val = data->val;
        cam_write(cam_handle, reg_addr, reg_val);
        data++;
    }
}

void spi_transmit(cam_handle_t *cam_handle, uint8_t data){
    spi_transaction_t data_transaction = {
        .length=8,
        .tx_buffer=&data
    };
    ESP_ERROR_CHECK(spi_device_queue_trans(cam_handle->cam_spi_handle, &data_transaction, portMAX_DELAY));
}

void spi_write(cam_handle_t *cam_handle, uint8_t addr, uint8_t value){
    spi_transaction_t set_addr = {
        .length=8,
        .tx_buffer=&addr
    };
    spi_transaction_t set_value = {
        .length=8,
        .tx_buffer=&value
    };

    ESP_ERROR_CHECK(spi_device_queue_trans(cam_handle->cam_spi_handle, &set_addr, portMAX_DELAY));
    ESP_ERROR_CHECK(spi_device_queue_trans(cam_handle->cam_spi_handle, &set_value, portMAX_DELAY));
}

void spi_write_read(cam_handle_t *cam_handle, uint8_t addr, uint8_t value, uint8_t *buf) {
    spi_transaction_t set_addr = {
        .length = 1,
        .tx_buffer = &addr,
        .flags = 0
    };

    spi_transaction_t set_value = {
        .length = 1,
        .tx_buffer = &value,
        .rx_buffer = buf,
        .flags = 0
    };

    esp_err_t ret;

    ret = spi_device_queue_trans(cam_handle->cam_spi_handle, &set_addr, pdMS_TO_TICKS(100));
    ESP_ERROR_CHECK(ret);


    ret = spi_device_queue_trans(cam_handle->cam_spi_handle, &set_value, pdMS_TO_TICKS(100));
    ESP_ERROR_CHECK(ret);
}

//add isinit check
void cam_init(cam_handle_t *cam_handle){
    ESP_ERROR_CHECK(i2c_master_probe(i2c_mst_bus_handle, ADDR >> 1, 1000));
    i2c_device_config_t dev_config = {
        .dev_addr_length= 7,
        .device_address = ADDR >> 1,
        .scl_speed_hz=100000
    };
    i2c_master_dev_handle_t cam_dev_handle;
    ESP_ERROR_CHECK(i2c_master_bus_add_device(i2c_mst_bus_handle, &dev_config, &cam_dev_handle));

    cam_handle->cam_i2c_handle=cam_dev_handle;


    cam_write_list(cam_handle, OV2640_JPEG_INIT);
    cam_write_list(cam_handle, OV2640_YUV422);
    cam_write_list(cam_handle, OV2640_JPEG);
    cam_write(cam_handle, 0xff, 0x01);
    cam_write(cam_handle, 0x15, 0x00);
    cam_write_list(cam_handle, OV2640_160x120_JPEG);

    spi_device_interface_config_t spi_dev_config = {
        .address_bits = 8,
        .clock_speed_hz = 4000000,
        .clock_source = SPI_CLK_SRC_DEFAULT,
        .command_bits = 8,
        .dummy_bits = 8,
        .mode=0,
        .cs_ena_posttrans=0,
        .spics_io_num=5,
        .queue_size=1
    };

    spi_device_handle_t cam_spi_handle;
    ESP_ERROR_CHECK(spi_bus_add_device(SPI2_HOST, &spi_dev_config, &cam_spi_handle));
    cam_handle->cam_spi_handle = cam_spi_handle;

    // uint8_t read_buffer;
    // spi_write_read(cam_handle, 0x00, 0x55, &read_buffer);
    // vTaskDelay(pdMS_TO_TICKS(100));
    // if (read_buffer != 0x55){
    //     ESP_LOGE("SPI", "SPI INTERFACE BORKE");
    //     ESP_ERROR_CHECK(ESP_ERR_NOT_FOUND);
    //     while(1);
    // }

    spi_write(cam_handle, ARDUCHIP_FIFO, FIFO_CLEAR_MASK);
    spi_write(cam_handle, ARDUCHIP_FIFO, FIFO_START_MASK);

    vTaskDelay(pdMS_TO_TICKS(100));
    uint8_t *img_buffer = calloc(256, sizeof(uint8_t));
    if(img_buffer == NULL){
        ESP_LOGE("SPI", "Failed to allocate memory for image buffer");
        ESP_ERROR_CHECK(ESP_ERR_NO_MEM);
    }

    spi_write_read(cam_handle, ARDUCHIP_FIFO, BURST_FIFO_READ, img_buffer);
    gpio_set_level(CS_PIN, 0);
    vTaskDelay(pdMS_TO_TICKS(1000));
    spi_transmit(cam_handle, 0xff);

    for (int i = 0; i < 100; i++) {
        ESP_LOGW("SPI", "Data[%d] = %02X", i, img_buffer[i]);
    }
    gpio_set_level(CS_PIN, 1);
    free(img_buffer);
}